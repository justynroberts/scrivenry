interface EmojiPickerProps {
  onSelect: (emoji: string) => void
  children: React.ReactNode
}

export function EmojiPicker({ onSelect, children }: EmojiPickerProps) {
  const [open, setOpen] = useState(false)
  const [search, setSearch] = useState('')

  const allEmojis = Object.values(EMOJI_CATEGORIES).flat()
  const filteredEmojis = search
    ? allEmojis.filter(() => true) // Simple filter - in real app would match emoji names
    : null

  const handleSelect = (emoji: string) => {
    onSelect(emoji)
    setOpen(false)
    setSearch('')
  }

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        {children}
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0" align="start" data-testid="emoji-picker">
        <div className="p-2 border-b">
          <Input
            placeholder="Search emoji..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="h-8"
            data-testid="emoji-search"
          />
        </div>
        <ScrollArea className="h-64">
          <div className="p-2">
            {filteredEmojis ? (
              <div className="grid grid-cols-8 gap-1">
                {filteredEmojis.map((emoji, i) => (
                  <Button
                    key={i}
                    variant="ghost"
                    className="h-8 w-8 p-0 text-lg"
                    onClick={() => handleSelect(emoji)}
                  >
                    {emoji}
                  </Button>
                ))}
              </div>
            ) : (
              Object.entries(EMOJI_CATEGORIES).map(([category, emojis]) => (
                <div key={category} className="mb-4">
                  <p className="text-xs font-medium text-muted-foreground mb-2">
                    {category}
                  </p>
                  <div className="grid grid-cols-8 gap-1">
                    {emojis.map((emoji, i) => (
                      <Button
                        key={i}
                        variant="ghost"
                        className="h-8 w-8 p-0 text-lg hover:bg-accent"
                        onClick={() => handleSelect(emoji)}
                        data-testid={`emoji-${emoji}`}
                      >
                        {emoji}
                      </Button>
                    ))}
                  </div>
                </div>
              ))
            )}
          </div>
        </ScrollArea>
        <div className="p-2 border-t">
          <Button
            variant="ghost"
            size="sm"
            className="w-full text-muted-foreground"
            onClick={() => handleSelect('')}
          >
            Remove emoji
          </Button>
        </div>
      </PopoverContent>
    </Popover>
  )
}
